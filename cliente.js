class Cliente{
    constructor(r, n, a, t, e, c, s, p, pr, cu, pg, dp){
        this.rut = r;
        this.nombre = n;
        this.apellido = a;
        this.telefono = t;
        this.email = e;
        this.ciudad = c;
        this.sexo = s;
        this.prenda = p;
        this.precio = pr;
        this.cuotas = cu;
        this.pago = pg;
        this.diapago = dp;
    }
}